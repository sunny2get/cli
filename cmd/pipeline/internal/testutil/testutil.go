package testutil

import (
	"bytes"
	"io"
	"os"
	"testing"
)

// CaptureStdout runs fn and returns everything written to os.Stdout as a string.
func CaptureStdout(t *testing.T, fn func()) string {
	t.Helper()

	old := os.Stdout
	r, w, _ := os.Pipe()
	os.Stdout = w

	fn()

	w.Close()

	os.Stdout = old

	var buf bytes.Buffer

	_, _ = io.Copy(&buf, r)

	return buf.String()
}
